async def tg_reply_keyboard(buttons: list):
    markup = ReplyKeyboardMarkup(resize_keyboard=True)
    for row in buttons:
        row_buttons = []
        for button in row:
            row_buttons.append(KeyboardButton(button))
        markup.row(*row_buttons)
    return markup


